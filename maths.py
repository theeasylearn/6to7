#example of mathematical operators 
num1 = float(input("Enter first number"))
num2 = float(input("Enter second number"))

add = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2
power = num1 ** num2 
floor_division = num1 // num2 
reminder = num1 % num2
message = f"add = {add} sub={sub} mul={mul} div={div} power={power} floor division = {floor_division} reminder={reminder}"
print(message)
